			survival is much more important in this environment.
		</p>
		<p>
			<b>10.</b> If for example you are about to execute a character and that character's friend shoots you from a distance
			without being in range to perform an action, then that is valid and you must not complain about the kill being random.
		</p>
		<p>
			<b>11.</b> Do not attempt to kill another character by using props, we have sufficient protection to prevent you from
