			<b>29.</b> The police can fine characters for commiting crimes instead of arresting them, if they wish, but if they can't pay it then you must
			arrest them. Whether you fine the character or not, you still need to tie them up and search them for illegal items.
		</p>
		<p>
			<b>30.</b> Police that aren't doing their jobs, like blatently ignoring a criminal in the street, will be blacklisted and demoted. There is a difference
			between letting sombody slide, and blatently ignoring the crime all together and not doing anything about it.
		</p>
		<p>
			<b>31.</b> Graffiti is illegal because spray cans are purchased from the black market. Anyone seen graffiting should be arrested.
		</p>
